int DivivsibleByFive(int iNo)
{
    int iAns = 0;
    iAns = iNo % 5; 

    if(iAns == 0)                           //  if(iAns != 0)
    {                                       //  {
        return 1;                           //      return 0;
    }                                       //  }
    else                                    //  else
    {                                       //  {
        return 0;                           //      return 1;
    }                                       //  }
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Entry Point function
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter a number :\n");
    scanf("%d",&iValue);

    iRet = DivivsibleByFive(iValue);
    
    if(iRet == 0)                           //  if(iRet != 0)
    {
        printf("%d is not divisible by 5\n",iValue);
    }
    else
    {
        printf("%d is divisible by 5\n",iValue);
    }

    return 0;
}

